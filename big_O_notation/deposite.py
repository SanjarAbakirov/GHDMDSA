# Простой калькулятор вклада

# Получаем данные
сумма = float(input("Сколько денег хотите положить? (USD): "))
проценты = float(input("Под какой процент? (% годовых): "))
годы = int(input("На сколько лет?: "))

# Рассчитываем
проценты_десятичные = проценты / 100
итого = сумма * (1 + проценты_десятичные * годы)
прибыль = итого - сумма

# Показываем результат
print(f"\nЧерез {годы} лет у вас будет: {итого:,.2f} USD")
print(f"Ваша прибыль составит: {прибыль:,.2f} USD")
